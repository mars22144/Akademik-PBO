    public void bersih(){
        jtxtnim.setText("");
        jtxtsiswa.setText("");
        jcbjurusan.setSelectedItem("--pilih--");   
    }
    
    public void tampildata(){
        DefaultTableModel tabelnya = new DefaultTableModel();
        
        tabelnya.addColumn("nim");
        tabelnya.addColumn("nama");
        tabelnya.addColumn("jurusan");
        
        try{
            koneksi();
            String sql = "Select * from tabelmahasiswa order by nim asc";
            ResultSet rs = cn.executeQuery(sql);
            while (rs.next()){
                tabelnya.addRow (new Object[]{
                   rs.getString(1),
                   rs.getString(2), 
                   rs.getString(3), 
                });
            }
            jTable1.setModel(tabelnya);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "ada kesalahan");
        }
    }
    
    public void cari(){
        try{
            koneksi();
            String sql = "Select * from tabelmahasiswa where nim='" + jtxtnim.getText()+ "'";
            ResultSet rs = cn.executeQuery(sql);
            
            if(rs.next()){
                jtxtnim.setText(rs.getString(1));
                jtxtsiswa.setText(rs.getString(2));
                jcbjurusan.setSelectedItem(rs.getString(3));
                
                JOptionPane.showMessageDialog(null, "Data mahasiswa : " + jtxtnim.getText() + "sudah ada");
                bersih();
            }
        }catch (Exception e){
            jtxtnim.requestFocus();
        }
    }

