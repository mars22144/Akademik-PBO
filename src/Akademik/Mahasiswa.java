    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        try {
            String sql = "update tabelmahasiswa set nama='" + jtxtsiswa.getText() +"'," + "jurusan='" + jcbjurusan.getSelectedItem() + "'"
            + "where nim='" + jtxtnim.getText() + "'";

            cn.executeUpdate(sql);
            conn.close();

            tampildata();
            bersih();
            jtxtnim.setEnabled(true);
            jtxtnim.requestFocus();
            jtxtsiswa.setEnabled(true);
            jcbjurusan.setEnabled(true);

            JOptionPane.showMessageDialog(null, "data berhasil dirubah");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "perubahan gagal");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_beditActionPerformed

